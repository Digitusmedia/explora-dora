import * as React from 'react';
import MapView, {Circle, Marker, PROVIDER_GOOGLE} from 'react-native-maps'
import {StyleSheet, Text, View, SafeAreaView, Dimensions, Button, TouchableOpacity} from 'react-native';
import * as Location from 'expo-location';


function MapPresenterFile({navigation}) {
    const [location, setLocation] = React.useState({
        latitude: 59.3322,
        longitude: 18.0642,
        latitudeDelta: 0.01,
        longitudeDelta: 0.01,
    });
    const [errorMsg, setErrorMsg] = React.useState(null);

    React.useEffect(() => {
        (async () => {
            let {status} = await Location.requestForegroundPermissionsAsync();
            if (status !== 'granted') {
                setErrorMsg('Permission to access location was denied');
            }
        })();
    }, []);

    React.useEffect(() => {
        Location.watchPositionAsync(
            {
                accuracy: Location.Accuracy.Highest,
                distanceInterval: 10,
                timeInterval: 10000,
            },
            (pos) => {
                pos.coords.latitudeDelta = 0.01;
                pos.coords.longitudeDelta = 0.01;
                setLocation(pos.coords);
                console.log("Continuous location: " + JSON.stringify(pos.coords))
            }
        )
            .then(() => {
            })
            .catch((err) => {
                console.log("position error: ", err.message);
                setErrorMsg(err.message);
            });
    }, []);

    return (
        <SafeAreaView style={styles.container}>
            <MapView region={location} showsUserLocation={true}
                     provider={PROVIDER_GOOGLE} style={styles.map} customMapStyle={customMap}>

                <Marker coordinate={location}
                        onPress={x => navigation.navigate("Take Picture")}/>
            </MapView>
            <Button
                title="Claim Landmark"
                onPress={x => navigation.navigate("Take Picture")}
            />
        </SafeAreaView>
    );
}

